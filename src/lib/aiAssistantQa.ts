export type AssistantMatchResult = {
  response: string;
  places?: AssistantPlaceCard[];
  table?: AssistantTable;
};

export function matchAssistantReply(userText: string): AssistantMatchResult {
  const t = userText.trim().toLowerCase();
  if (!t) return { response: DEFAULT_REPLY };

  let best: { score: number; entry: QaEntry } | null = null;
  for (const entry of qaDatabase) {
    let score = 0;
    for (const kw of entry.keywords) {
      if (t.includes(kw.toLowerCase())) score += 1;
    }
    if (score > 0 && (!best || score > best.score)) best = { score, entry };
  }

  if (!best) return { response: DEFAULT_REPLY };
  const { entry } = best;
  return {
    response: entry.response,
    places: entry.places,
    table: entry.table,
  };
}
